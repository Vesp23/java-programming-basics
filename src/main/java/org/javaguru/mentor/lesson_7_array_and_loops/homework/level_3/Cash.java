package org.javaguru.mentor.lesson_7_array_and_loops.homework.level_3;

public class Cash {
    private int number;
    private int summary;

    public Cash(int number, int summary) {
        this.number = number;
        this.summary = summary;
    }

    public int getNumber() {
        return number;
    }

    public int getSummary() {
        return summary;
    }
}
